#include "minishell.h"

void ft_unset(t_cmd *cmd, t_env **env)
{
    int i = 1;

    if (!cmd->args[i])
        return ;

    while (cmd->args[i])
    {
        unset_env_var(env, cmd->args[i]);
        i++;
    }
}
